SELECT R.TMC_LIST_ID  
      ,(SELECT FIRST 1 COALESCE(T0000.NAME, '<...>') FROM TABL$R_TMC_LIST T0000 WHERE (T0000.ID = R.TMC_LIST_ID)) AS TMC_LIST_ID_ 
      ,R.FLAG_DELETE  
      ,R.ID  
      ,( R.TMC_ID) AS TMC_ID2 
      ,R.TMC_ID  
      ,TMC.NAME AS TMC_ID_ 
      ,TMC.TMC_LIST_ID AS TMC_TMC_LIST_ID 
      ,(SELECT FIRST 1 COALESCE(N014.NAME,'') FROM TABL$R_TMC_TYPES N014 WHERE (N014.ID = TMC.TMC_TYPE_ID)) AS TMC_TYPE 
      ,TMC.ARTICLE AS TMC_ARTICLE 
      ,TMC.NUMSHOW AS TMC_NUMSHOW 
      ,(SELECT FIRST 1 C003.VALUTE FROM TABL$R_COUNTRIES C003 WHERE (C003.ID = TMC.COUNTRY_ID)) AS TMC_COUNTRY 
      ,TMC.PROBE AS TMC_PROBE 
      ,TMC.MASSA AS TMC_MASSA 
      ,(TMC.MASSA - TMC.MASSA_NETTO) AS TMC_MASSA_INS 
      ,TMC.MASSA_NETTO AS TMC_MASSA_NETTO 
      ,(SELECT FIRST 1 E008.SHORT_NAME FROM  TABL$R_EDIZM E008 WHERE (E008.ID = TMC.EDIZM_ID)) AS TMC_EDIZM 
      ,(SELECT FIRST 1 P009.PRICE FROM TABL$R_TMC_P P009 WHERE(P009.TMC_ID = TMC.ID)AND(P009.VALUE_DATE = TMC.VALUE_DATE)) AS TMC_PRICE 
      ,R.QUANT  
      ,(SELECT FIRST 1 P010.PRICE *  R.QUANT FROM TABL$R_TMC_P P010 WHERE(P010.TMC_ID = TMC.ID)AND(P010.VALUE_DATE = TMC.VALUE_DATE)) AS TOTAL 
      ,(TMC.MASSA *  R.QUANT) AS TC_MASSA 
      ,((TMC.MASSA - TMC.MASSA_NETTO) *  R.QUANT) AS TC_MASSA_INS 
      ,(TMC.MASSA_NETTO *  R.QUANT) AS TC_MASSA_NETTO 
      ,R.NAME  
FROM   TABL$R_TMC_LISTQUANT R, TABL$R_TMC TMC  
WHERE  (R.TMC_LIST_ID = ?TMC_LIST_ID)
  AND  (TMC.ID = R.TMC_ID)
