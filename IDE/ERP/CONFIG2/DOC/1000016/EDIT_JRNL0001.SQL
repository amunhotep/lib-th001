      ,(SELECT FIRST 1 T0004.NAME FROM TABL$R_EXPTYPES T0004 WHERE (T0004.ID = R0001.EXPTYPE_ID)) AS EXPTYPE_ID_
      ,R0001.DOC_ID
      ,(SELECT FIRST 1 T0005.DOCSTR FROM TABL$J_4 T0005 WHERE (T0005.ID = R0001.DOC_ID)) AS DOC_ID_
      ,R0001.REASON
      ,R0001.ENCLOSURE
      ,R0001.ADDRESS_ID
      ,(SELECT FIRST 1 T0004.NAME_FULL FROM TABL$R_ADDRESS T0004 WHERE (T0004.ID = R0001.ADDRESS_ID)) AS ADDRESS_NAME
