      ,R0000.DOCNUMBER
      ,R0000.DOCNUMBERSTR
      ,R0000.DOCNUMBERIN
      ,R0001.FLAG_DEBET
      ,R0001.PAYSRC_ID
      ,(SELECT FIRST 1 T0002.NAME FROM TABL$R_PAYSRC T0002 WHERE (T0002.ID    = R0001.PAYSRC_ID)) AS PAYSRC_ID_
