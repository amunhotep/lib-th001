      ,R0000.FLAG_DELETE
      ,R0000.FLAG_COMMIT
      ,R0000.FLAG_LOCK
      ,R0000.ID
      ,R0000.TYPE_ID
      ,(SELECT FIRST 1 T0001.NAME FROM TABL$_TB_DOCS T0001 WHERE (T0001.ID = R0000.TYPE_ID)) AS TYPE_ID_
