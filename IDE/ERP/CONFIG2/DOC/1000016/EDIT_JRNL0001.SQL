      ,R0000.FIRM_ID
      ,(SELECT FIRST 1 T0007.NAME FROM TABL$R_FIRMS T0007 WHERE (T0007.ID = R0000.FIRM_ID)) AS FIRM_ID_
      ,R0000.FILIAL_ID
      ,(SELECT FIRST 1 T0008.NAME FROM TABL$R_FILIALS T0008 WHERE (T0008.ID = R0000.FILIAL_ID)) AS FILIAL_ID_
      ,R0000.USER_ID
      ,(SELECT FIRST 1 T0009.ID FROM TABL$_USERS T0009 WHERE (T0009.ID = R0000.USER_ID)) AS USER_ID_
      ,(SELECT FIRST 1 COALESCE(WZZ.NAME, R0000.USER_ID) FROM TABL$R_WRK WZZ WHERE (TRIM(WZZ.USER_NAME) = R0000.USER_ID) ) AS WRK_NAME
      ,R0000.DOCSTR
      ,(SELECT FIRST 1 TBD01.COLOR_FRG FROM TABL$_TB_DOCS TBD01 WHERE (TBD01.ID = R0000.TYPE_ID)) AS COLOR_FRG
      ,(SELECT FIRST 1 TBD01.COLOR_BGR FROM TABL$_TB_DOCS TBD01 WHERE (TBD01.ID = R0000.TYPE_ID)) AS COLOR_BGR 
FROM   TABL$J_4 R0000, TABL$J_1000016 R0001 
WHERE (R0000.ID = ?ID)
  AND (R0001.J_ID = R0000.ID) 
