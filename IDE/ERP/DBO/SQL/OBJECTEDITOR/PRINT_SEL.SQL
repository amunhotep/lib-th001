EXECUTE BLOCK (
   Q_OBJ_ID    TYPE OF COLUMN TABL$_TB.ID           = ?OBJ_ID
  ,Q_OBJ_TYPE  TYPE OF COLUMN TABL$_TB.BASE_TYPE_ID = ?OBJ_TYPE
)RETURNS (
   OBJ_ID      TYPE OF COLUMN TABL$_TB.ID
  ,OBJ_TYPE    TYPE OF COLUMN TABL$_TB.BASE_TYPE_ID
  ,ID          TYPE OF COLUMN TABL$_TB_DOCSRPT.ID
  ,NAME        TYPE OF COLUMN TABL$_TB_DOCSRPT.NAME
  ,FLAG_DELETE TYPE OF COLUMN TABL$_TB_DOCSRPT.FLAG_DELETE
  ,ORDERID     TYPE OF COLUMN TABL$_TB_DOCSRPT.ORDERID
  ,SUBTYPE     TYPE OF COLUMN TABL$_TB_DOCSRPT.SUBTYPE
  ,PATH        TYPE OF COLUMN TABL$_TB_DOCSRPT.PATH
  ,DESCR       TYPE OF COLUMN TABL$_TB_DOCSRPT.DESCR
)
AS
BEGIN
  OBJ_ID   = :Q_OBJ_ID;
  OBJ_TYPE = :Q_OBJ_TYPE;
  FOR
    SELECT T.ID, T.NAME, T.FLAG_DELETE, T.ORDERID, T.SUBTYPE, T.PATH, T.DESCR
    FROM   TABL$_TB_DOCSRPT T
    WHERE  (T.OBJ_ID = :OBJ_ID)
    ORDER BY T.SUBTYPE, T.ORDERID
    INTO   :ID, :NAME, :FLAG_DELETE, :ORDERID, :SUBTYPE, :PATH, :DESCR
  DO
    SUSPEND;
END
