SELECT TMCMASTER.ID AS CALC_TMC_ID, TMCMASTER.NAME AS CALC_TMC_NAME, TMCMASTER.NAME2 AS CALC_TMC_NAME2, 
       TMCMASTER.ARTICLE AS CALC_TMC_ARTICLE, TMCMASTER.COMENT AS CALC_TMC_COMENT, TMCMASTER.DATAXML AS 
       CALC_TMC_DATAXML, TMCMASTER.EDIZM_ID AS CALC_TMC_EDIZM_ID, 
       EMASTER.SHORT_NAME AS CALC_TMC_ESHORT, PMASTER.PRICE AS CALC_TMC_PRICE, TLQ.TMC_LIST_ID AS 
       CALC_TMC_LIST_ID, TLQ.ID AS TLQ_ID, TLQ.TMC_ID AS TMC_ID, TMC.NAME AS TMC_NAME, TMC.NAME2 AS 
       TMC_NAME2, TMC.ARTICLE AS TMC_ARTICLE, TMC.TMC_LIST_ID AS TMC_LIST_ID, ETMC.SHORT_NAME AS TMC_ESHORT, 
       P.PRICE AS TMC_PRICE, TLQ.QUANT AS TLQ_QUANT, CAST((TLQ.QUANT * P.PRICE) AS DOMN$FLOAT) AS TLQ_TOTAL, TLQ.NAME AS TLQ_NAME 
FROM   TABL$R_TMC TMCMASTER, TABL$R_EDIZM EMASTER, TABL$R_TMC_P PMASTER,  TABL$R_TMC_LISTQUANT TLQ, 
       TABL$R_TMC TMC, TABL$R_EDIZM ETMC, TABL$R_TMC_P P 
WHERE  (TMCMASTER.ID = ?TMC_ID) 
  AND  (EMASTER.ID = TMCMASTER.EDIZM_ID)  
  AND  (PMASTER.TMC_ID = TMCMASTER.ID) 
  AND  (PMASTER.VALUE_DATE = TMCMASTER.VALUE_DATE) 
  AND  (TLQ.TMC_LIST_ID = TMCMASTER.TMC_LIST_ID) 
  AND  (TMC.ID = TLQ.TMC_ID) 
  AND  (ETMC.ID = TMC.EDIZM_ID) 
  AND  (P.TMC_ID = TMC.ID) 
  AND  (P.VALUE_DATE = TMC.VALUE_DATE)
