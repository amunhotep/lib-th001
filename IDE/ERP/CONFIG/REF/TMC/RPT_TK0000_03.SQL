      ,R.ID  
      ,R.TMC_ID  
      ,(SELECT FIRST 1 COALESCE(T0002.NAME, '<...>') FROM TABL$R_TMC T0002 WHERE (T0002.ID = R.TMC_ID)) AS TMC_ID_ 
      ,R.FIRM_ID  
      ,(SELECT FIRST 1 COALESCE(T0003.NAME, '<...>') FROM TABL$R_FIRMS T0003 WHERE (T0003.ID = R.FIRM_ID)) AS FIRM_ID_ 
      ,R.FILIAL_ID  
      ,(SELECT FIRST 1 COALESCE(T0004.NAME, '<...>') FROM TABL$R_FILIALS T0004 WHERE (T0004.ID = R.FILIAL_ID)) AS FILIAL_ID_ 
      ,R.PLACE_ID  
      ,(SELECT FIRST 1 COALESCE(T0005.NAME, '<...>') FROM TABL$R_PLACES T0005 WHERE (T0005.ID = R.PLACE_ID)) AS PLACE_ID_ 
      ,R.QUANT  
      ,R.NAME  
FROM   TABL$R_TMC_LIMITS R  
WHERE (R.TMC_ID = ?ID)
