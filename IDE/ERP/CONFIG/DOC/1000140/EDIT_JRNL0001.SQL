SELECT R0000.ID AS DS$ID 
      ,R0000.FLAG_DELETE
      ,R0000.FLAG_COMMIT
      ,R0000.FLAG_LOCK
      ,R0000.ID
      ,R0000.TYPE_ID
      ,(SELECT FIRST 1 T0001.NAME FROM TABL$_TB_DOCS T0001 WHERE (T0001.ID = R0000.TYPE_ID)) AS TYPE_ID_
      ,R0000.DOCNUMBER
      ,R0000.DOCNUMBERSTR
      ,R0000.DOCNUMBERIN
      ,R0000.VALUTE_ID
      ,(SELECT FIRST 1 T0011.VALUTE_SHORT FROM TABL$R_COUNTRIES T0011 WHERE (T0011.ID = R0000.VALUTE_ID)) AS VALUTE_ID_
      ,R0000.DOCSUMVAL
      ,R0000.DOCSUM
      ,R0000.NAME
      ,R0000.DATE_COMMIT
      ,(EXTRACT(YEAR FROM R0000.DATE_COMMIT)) AS DATE_COMMIT_YEAR
      ,(EXTRACT(MONTH FROM R0000.DATE_COMMIT)) AS DATE_COMMIT_MONTH
      ,(EXTRACT(DAY FROM R0000.DATE_COMMIT)) AS DATE_COMMIT_DAY
      ,R0000.FIRM_ID
      ,(SELECT FIRST 1 T0012.NAME FROM TABL$R_FIRMS T0012 WHERE (T0012.ID = R0000.FIRM_ID)) AS FIRM_ID_
      ,R0000.FILIAL_ID
      ,(SELECT FIRST 1 T0013.NAME FROM TABL$R_FILIALS T0013 WHERE (T0013.ID = R0000.FILIAL_ID)) AS FILIAL_ID_
      ,R0000.USER_ID
      ,(SELECT FIRST 1 T0014.ID FROM TABL$_USERS T0014 WHERE (T0014.ID = R0000.USER_ID)) AS USER_ID_
      ,(SELECT FIRST 1 COALESCE(WZZ.NAME, R0000.USER_ID) FROM TABL$R_WRK WZZ WHERE (TRIM(WZZ.USER_NAME) = R0000.USER_ID) ) AS WRK_NAME
      ,R0000.DOCSTR
      ,(SELECT FIRST 1 TBD01.COLOR_FRG FROM TABL$_TB_DOCS TBD01 WHERE (TBD01.ID = R0000.TYPE_ID)) AS COLOR_FRG
      ,(SELECT FIRST 1 TBD01.COLOR_BGR FROM TABL$_TB_DOCS TBD01 WHERE (TBD01.ID = R0000.TYPE_ID)) AS COLOR_BGR 
FROM   TABL$J_4 R0000 
WHERE (R0000.ID = ?ID)
